class calculator:

	def addition(x,y):
		add = x + y
		print(add)

	def subtraction(x,y):
		sub = x - y
		print(sub)

	def multiplication(x,y):
		mul = x * y
		print(mul)

	def division(x,y):
		div = x / y
		print(div)

calculator.addition(3,5)
calculator.subtraction(3,5)
calculator.multiplication(3,5)
calculator.division(3,5)